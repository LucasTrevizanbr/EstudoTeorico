package capitulo3;
/*
    Variáveis de tipos primitivos em java possuem tamanho e temos que especifica-los
 */
public class TiposPrimitivosTamanho {

    //Lógico
    boolean variavelBooleana = true;//0 ou 1, verdadeiro ou falsa
    char varialVelDoTipoChar = 'l';//16 bits de tamanho

    //Inteiro
    byte umaVariavelDoTipoByte = 8; //Byte são pequenas com 8 bites de espaço
    short umaVariavelDoTipoShort = 16;//shorts são curtas com 16 bits de espaço
    int umaVariavelDoTipoInt = 32;//int ja são consideradas grandes para média e tem 32 bits de espaço
    long umaVariavelDoTipoLong = 64; //Agora estamos falando de um valor ENORME com 64bits de espaço

    //Ponto flutuante
    float umaVariavelDoTipoFloat = 22.5f;//32 bits de tamanho e seu valor varia
    double umaVariavelDoTipoDouble = 666.666;//64 bits de tamanho e seu valor varia
}
